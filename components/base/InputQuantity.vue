<template>
  <div class="input-quantity">
    <UButton
      color="white"
      icon="i-heroicons:minus"
      variant="none"
      @click="handleDecrease" class="dark:text-black/80"
    />
    <UInput v-model="model" type="number" :padded="false" variant="none" class="dark:text-black/80" />
    <UButton
      color="white"
      icon="i-heroicons:plus"
      variant="none"
      @click="handleIncrease" class="dark:text-black/80"
    />
  </div>
</template>

<script setup>
const props = defineProps({
  min: {
    type: Number,
    default: 1,
  },
  max: {
    type: Number,
    default: 10,
  },
});

const model = defineModel({
  default: 1,
  type: Number,
});

function handleDecrease() {
  if (model.value > props.min) {
    model.value--;
  }
}

function handleIncrease() {
  if (model.value < props.max) {
    model.value++;
  }
}
</script>

